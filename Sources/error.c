#include <stdio.h>
#include <stdlib.h>
#include <string.h>

/*
 =======================================================================================================================
 =======================================================================================================================
 */
void Fatal(char *_psz_Text, char *_psz_Param)
{
	printf("** Fatal Error: %s", _psz_Text);
	if(_psz_Param) printf(" ==> %s", _psz_Param);
	printf("\n");
	exit(-1);
}

/*
 =======================================================================================================================
 =======================================================================================================================
 */
void Warning(char *_psz_Text, char *_psz_Param)
{
	printf("** Warning: %s", _psz_Text);
	if(_psz_Param) printf(" ==> %s", _psz_Param);
	printf("\n");
}

/*
 =======================================================================================================================
 =======================================================================================================================
 */
void Syntaxe(int line, int col)
{
	printf("** Syntax error detected or file too strange... \n Trying to match Line %d col %d \n Exit.\n", line, col);
	exit(-1);
}
